<?php

$x = 0;

switch ($x) {
  case 0:
    echo "X é igual a 0 <br>";
    break;
  case 1:
    echo "X é igual a 1 <br>";
    break;
  default:
    echo "X não é igual a nenhum dos valores mencionados <br>";
}

$y = "Pedro";

switch ($y) {
  case "Pedro":
    echo "O nome é Pedro";
    break;
  case "João":
    echo "O nome é João";
    break;
  default:
    echo "X não é igual a nenhum dos valores mencionados <br>";
}